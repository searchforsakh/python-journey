print("MASUKKAN SUHU DALAM CELCIUS")
celcius = float(input("Masukkan suhu dalam celcius = "))
print(celcius, "adalah suhu dalam celcius")

reamur = 4/5 * celcius
print(reamur, "adalah suhu dalam reamur")

fahrenreit = 9/5*celcius + 32
print(f"{fahrenreit}, adalah suhu dalam fahrenreit")

kelvin = celcius + 273
print(kelvin, "adalah suhu dalam kelvin")